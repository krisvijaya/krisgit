package rohit;

public class Foreachloop 
{

	public static void main(String[] args) 
	{
        int arr[]={2,11,45,9,39,56};
        //i starts with 0 as array index starts with 0 too
        for(int i=0; i<arr.length; i++)
        {
             System.out.println(arr[i]);
        }


	}

}
